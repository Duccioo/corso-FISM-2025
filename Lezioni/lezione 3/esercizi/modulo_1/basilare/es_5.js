// Area rettangolo: Crea una funzione che calcola l'area di un rettangolo date base e altezza come parametri.

// Soluzione
function areaRettangolo(base, altezza) {
  let area = base * altezza;
  return area;
}

// Test
console.log(areaRettangolo(5, 8)); // Output: 40
console.log(areaRettangolo(4, 3)); // Output: 12
