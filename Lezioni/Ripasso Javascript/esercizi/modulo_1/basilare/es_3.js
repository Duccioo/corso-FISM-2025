// Concatenazione stringhe: Scrivi una funzione che prende nome e cognome come parametri e restituisce un saluto formale (es. "Buongiorno, Mario Rossi!").

// Soluzione
function salutoFormale(nome, cognome) {
  return `Buongiorno, ${nome} ${cognome}!`;
}

// Test
console.log(salutoFormale("Mario", "Rossi")); // Output: Buongiorno, Mario Rossi!
